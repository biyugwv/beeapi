type VerifyImg struct
{
	W int
	H int
	S int
	Code string
	Img *image.RGBA
}

func (i *VerifyImg) Create() string{
	
	img := image.NewRGBA(image.Rect(0, 0, i.W, i.H))
	i.Img = img
	// 如何计算文字的位置
	if(i.Code ==""){
		i.Code = strconv.Itoa(rand.Intn(9000) + 1000);
	}
	scale := i.H/i.S
	length := len(i.Code)
	if((i.S+10)*length>i.W){
		return "err----code is too long----err"
	}
	space := 20;
	startX := (i.W - (i.S+space)*length+space)/2
	startY := (i.H-i.S)/2
	code := []rune(i.Code)
	for _,v := range code{
		index, err := strconv.Atoi(string(v))
		if(err!=nil){
			return "err----code is not  number "+i.Code+"----err"
		}
		//fmt.Println(startX,startY)
		i.drawNum(index,scale,startX,startY)
		startX+=i.S+space
	}
	
	for x:=0;x<i.W;x++{
		for y:=0;y<i.H;y++{
			if(x == 0 || y == 0 || x == i.W-1 || y == i.H-1) {
				img.Set(x,y,color.RGBA{0, 0, 0, 100})
			}
		}
	}
	for x:=0;x<10;x++ {
		i.drawCircle(rand.Intn(i.W),rand.Intn(i.H),rand.Intn(2)+2,randColor())
	}
	i.drawLine(10,i.W-10,i.H/2)
	emptyBuff := bytes.NewBuffer(nil)
	png.Encode(emptyBuff, img)
        fmt.Println(emptyBuff.Cap(),emptyBuff.Len())
        n := base64.StdEncoding.EncodedLen(emptyBuff.Len())
	dist := make([]byte,n )
	
	base64.StdEncoding.Encode(dist, emptyBuff.Bytes())
	
	return "data:image/png;base64," + string(dist)
}

func (i *VerifyImg) drawCircle(x0,y0 ,r int,c color.RGBA) {
	
	for x:=x0-r;x<x0+r+1;x++{
		for y:=y0-r;y<y0+r+1;y++{
			if(math.Sqrt(float64((x0-x)*(x0-x))+float64((y0-y)*(y0-y)))<=float64(r)){
				i.Img.Set(x,y,c)
			}
		}
	}
}

func (i *VerifyImg) drawNum(index ,scale,sx,sy int){
	c := randColor();
	for k,v := range font[index]{
		if(v != 1){
			continue
		}
		x := sx + (k%11)*scale
		y := sy + (k/11)*scale/2
		x += rand.Intn(2)-1
		y += rand.Intn(2)-1
		//fmt.Println(k,x,y)
		if(k>=0){
			//i.drawCircle(x,y,1,c)
			i.Img.Set(x,y,c)
		}
	}
}


func (i *VerifyImg) drawLine(x0,x1,y int){
	
	xtmp := x0
	c := randColor()
	for {
		d := rand.Intn(1)
		i.drawCircle(xtmp,y+rand.Intn(4)-2,d+1,c)
		xtmp+=d+rand.Intn(2)+2
		if(xtmp>=x1) {
			break
		}
	}
}

func randColor() color.RGBA{
	circleCorlor := uint8(rand.Intn(100) + 100 )
	circleCorlorType := rand.Intn(3)
	var c1,c2,c3,c4 uint8
	c4 = uint8(rand.Intn(100) + 100)
	if( circleCorlorType == 0 ){
		c1 = circleCorlor
		c2 = uint8(0)
		c3 = uint8(0)
	}else if(circleCorlorType == 1){
		c1 = uint8(0)
		c2 = circleCorlor
		c3 = uint8(0)
	}else{
		c1 = uint8(0)
		c2 = uint8(0)
		c3 = circleCorlor
	}
	return color.RGBA{c1,c2,c3,c4}
}

